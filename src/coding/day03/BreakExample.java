package coding.day03;

import java.util.Scanner;

public class BreakExample {
	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		int num = 0;

		while (true) {
			if (in.nextInt() == -1) { // -1이 입력되면 반복 종료
				break;
			}
			num++;
		}
		System.out.println("입력된 숫자 개수는 " + num);
	}
}
